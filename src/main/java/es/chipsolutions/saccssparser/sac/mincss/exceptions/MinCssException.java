package es.chipsolutions.saccssparser.sac.mincss.exceptions;

public class MinCssException extends Exception{

	public MinCssException() {
		super();
	}

	public MinCssException(String message, Throwable cause) {
		super(message, cause);
	}

	public MinCssException(String message) {
		super(message);
	}

	public MinCssException(Throwable cause) {
		super(cause);
	}

}
